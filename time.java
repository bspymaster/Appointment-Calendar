public class time {
    
    private int hour;
    private int minute;
    
    time(int h, int m) {
        hour = h;
        minute = m;
    }
    
    public int getHour(){
        return hour;
    }
    
    public int getMinute(){
        return hour;
    }
    
    public void setHour(int h){
        hour = h;
    }
    public void SetMinute(int m){
        minute = m;
    }
    
}
